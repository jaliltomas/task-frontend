<template>
  <div class="card p-4 flex items-start gap-4">
    <!-- Status Icon -->
    <div 
      class="w-10 h-10 rounded-xl flex items-center justify-center flex-shrink-0"
      :class="statusColors"
    >
      <svg v-if="message.status === 'processed'" class="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
        <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M5 13l4 4L19 7" />
      </svg>
      <svg v-else-if="message.status === 'ignored'" class="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
        <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M18.364 18.364A9 9 0 005.636 5.636m12.728 12.728A9 9 0 015.636 5.636m12.728 12.728L5.636 5.636" />
      </svg>
      <svg v-else class="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
        <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M12 8v4l3 3m6-3a9 9 0 11-18 0 9 9 0 0118 0z" />
      </svg>
    </div>
    
    <!-- Content -->
    <div class="flex-1 min-w-0">
      <div class="flex items-center gap-2 mb-1">
        <span class="font-medium text-slate-800">
          {{ message.provider?.name || 'Desconocido' }}
        </span>
        <span :class="badgeClass">
          {{ statusLabel }}
        </span>
        <span v-if="message.products_count > 0" class="badge badge-info">
          {{ message.products_count }} productos
        </span>
      </div>
      
      <p class="text-sm text-slate-600 line-clamp-2">
        {{ truncatedContent }}
      </p>
      
      <p class="text-xs text-slate-400 mt-2">
        {{ formatDate(message.created_at) }}
      </p>
    </div>
    
    <!-- Phone Link -->
    <a 
      v-if="message.phone_number" 
      :href="`https://wa.me/${message.phone_number}`"
      target="_blank"
      class="text-emerald-600 hover:text-emerald-700 p-2"
    >
      <svg class="w-5 h-5" fill="currentColor" viewBox="0 0 24 24">
        <path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413z"/>
      </svg>
    </a>
  </div>
</template>

<script setup>
import { computed } from 'vue'

const props = defineProps({
  message: {
    type: Object,
    required: true
  }
})

const statusColors = computed(() => {
  switch (props.message.status) {
    case 'processed':
      return 'bg-emerald-100 text-emerald-600'
    case 'ignored':
      return 'bg-slate-100 text-slate-500'
    default:
      return 'bg-amber-100 text-amber-600'
  }
})

const badgeClass = computed(() => {
  switch (props.message.status) {
    case 'processed':
      return 'badge badge-success'
    case 'ignored':
      return 'badge bg-slate-100 text-slate-600'
    default:
      return 'badge badge-warning'
  }
})

const statusLabel = computed(() => {
  switch (props.message.status) {
    case 'processed':
      return 'Procesado'
    case 'ignored':
      return 'Ignorado'
    default:
      return 'Pendiente'
  }
})

const truncatedContent = computed(() => {
  const content = props.message.content || ''
  return content.length > 150 ? content.substring(0, 150) + '...' : content
})

function formatDate(dateStr) {
  if (!dateStr) return ''
  return new Intl.DateTimeFormat('es-AR', {
    day: '2-digit',
    month: '2-digit',
    year: 'numeric',
    hour: '2-digit',
    minute: '2-digit'
  }).format(new Date(dateStr))
}
</script>
